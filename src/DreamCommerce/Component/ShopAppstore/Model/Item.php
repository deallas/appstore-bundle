declare(strict_types=1);

namespace DreamCommerce\Component\ShopAppstore\Model;

class Item extends DataContainer implements ItemInterface
{
    /**
     * @var int|null
     */
    private $id;

    /**
     * {@inheritdoc}
     */
    public function getId()
    {
        return $this->id;
    }

    /**
     * @param int $id
     */
    public function setId(int $id)
    {
        $this->id = $id;
    }
}